package com.aksu.grup32java.ders3;

public class BasitIfYapısı {

    public void basitIf(int yas){
        if (yas==20)
            System.out.println("Yaşınız 30");

    }

    public void basitIfYapisi(int yas){
        if(yas>0 && yas <12)
        System.out.println("Çocuksunuz");

        if(yas>11 && yas <20)
            System.out.println("Gençsiniz");

        if(yas>20)
            System.out.println("Yetişkinsiniz");
        }

    public void basitIfElse(int yas){
        if(yas==20)
            System.out.println("Yaşınız 20");

        else
            System.out.println("Yaşınız 20 değil");

    }
    }


